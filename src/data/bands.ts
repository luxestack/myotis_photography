export function getBandBySlug(slug: string): Band | undefined {
  return bands.find((b) => b.slug === slug);
}

export function getAdjacentBands(slug: string) {
  const index = bands.findIndex((b) => b.slug === slug);
  if (index === -1) return { prev: undefined, next: undefined };
  const prev = index > 0 ? bands[index - 1] : bands[bands.length - 1];
  const next = index < bands.length - 1 ? bands[index + 1] : bands[0];
  return { prev, next };
}
